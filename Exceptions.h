#pragma once

////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdexcept>

////////////////////////////////////////////////////////////////////////////////////////////////////

class Socket;

////////////////////////////////////////////////////////////////////////////////////////////////////

class SocketException : public std::exception
{
public:
	SocketException(Socket *socket, const char *const &description, int error = 0) : std::exception(description),
																					 socket_(socket),
																					 error_(error)
	{

	};

	Socket *socket_;
	int error_;
};

////////////////////////////////////////////////////////////////////////////////////////////////////

class SocketDisconnectedException : public SocketException
{
public:
	SocketDisconnectedException(Socket *socket, const char *const &description, int error) : SocketException(socket, description, error)
	{

	};
};

